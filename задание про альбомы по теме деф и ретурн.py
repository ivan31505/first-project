def make_album(исполнитель, название):
    return {
        'певец': исполнитель,
        'песня': название
    }


# Создаём три альбома (три словаря)
альбом1 = make_album("The Beatles", "Abbey Road")
альбом2 = make_album("Pink Floyd", "The Dark Side of the Moon")
альбом3 = make_album("Queen", "A Night at the Opera")

# Выводим все возвращаемые значения для проверки

print("=== Проверка сохранения данных в словарях ===\n")

print("Альбом 1:")
print(f"  Исполнитель: {альбом1['певец']}")
print(f"  Название:  {альбом1['песня']}")


print("\nАльбом 2:")
print(f"  Исполнитель: {альбом2['певец']}")
print(f"  Название:  {альбом2['песня']}")


print("\nАльбом 3:")
print(f"  Исполнитель: {альбом3['певец']}")
print(f"  Название:  {альбом3['песня']}")

print("\n=== Все данные успешно сохранены! ===")
			
